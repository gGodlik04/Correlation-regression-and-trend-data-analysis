    [56, 44, 3, 10, 9, 3000, 6.900],
    [28, 21, 12, 12, 15, 3500, 4.985],
    [68, 56, 4, 12, 8, 800, 8.120],
    [88, 79, 2, 4, 4, 300, 11.900],
    [82, 50, 3, 5, 9.5, 2000, 10.000],
    [78, 64, 9, 10, 6.8, 1700, 9.300],
