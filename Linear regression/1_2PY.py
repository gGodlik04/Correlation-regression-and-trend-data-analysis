# Создание и обучение модели линейной регрессии
model = LinearRegression()
model.fit(X, y)

# Ввод параметров с клавиатуры
totsquare = float(input("Введите площадь (totsquare): "))
livesquare = float(input("Введите жилую площадь (livesquare): "))
